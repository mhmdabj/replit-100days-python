def lesson9(year):
    if(year >= 1925 and year <= 1946):
        print("Traditionalists yooo, rip bro")
    elif(year >= 1947 and year <= 1964):
        print("Baby Boomers, haha grandiesss")
    elif(year >= 1965 and year <= 1981):
        print("Generation X, what a generation")
    elif(year >= 1982 and year <= 1995):
        print("Millenials, take it easy 30+ haha")
    elif(year >= 1996 and year <= 2015):
        print("Generation Z, im 1997 sad that im in here lol")
    else:
        print("Year out of range")

